 * @method static \Illuminate\Database\Eloquent\Builder|User whereTwoFactorRecoveryCodes($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereTwoFactorSecret($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereUpdatedAt($value)
 * @mixin \Eloquent
 */
class User extends Authenticatable implements FilamentUser {
    use HasApiTokens, HasFactory, HasProfilePhoto, Notifiable, TwoFactorAuthenticatable, HasRoles, HasPrefixedId;

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    protected $guarded = [];
    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
        'two_factor_recovery_codes',
        'two_factor_secret',
    ];

    public
    function canAccessFilament(): bool {
        return ($this->hasRole(InternalRoles::ADMIN()) || $this->hasRole(InternalRoles::SUPER_ADMIN())) &&
               $this->hasVerifiedEmail();
    }

    /**
     * Get the route key for the model.
     */
    public
    function getRouteKeyName(): string {
        return config("prefixed-ids.prefixed_id_attribute_name");
    }

    public
    function reservations(): HasMany {
        return $this->hasMany(Reservation::class);
    }
}
